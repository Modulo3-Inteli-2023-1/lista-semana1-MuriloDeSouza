def multiplas_operações(a, b):

    soma =  a+b
    subtracao =  a-b
    multiplicação =  a*b
    if b==0: 
        divisao = 0
    else:
        divisao = a/b
    return soma, subtracao, multiplicação, divisao #aqui ele retorna as operações matematicas





